"""Evaluation result API.

The endpoint only returns aggregated metrics generated by
`scripts/eval_golden_set.py`; raw Golden Set questions, SQL templates and
retrieval snippets remain in ignored local files.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

from fastapi import APIRouter, HTTPException

from ..core import get_settings

router = APIRouter(prefix="/api/evaluation", tags=["evaluation"])


def _percent(value: float | int | None) -> str:
    if value is None:
        return "-"
    return f"{float(value) * 100:.0f}%"


def _load_eval_file(path: Path) -> dict[str, Any]:
    if not path.exists():
        raise HTTPException(
            status_code=404,
            detail=(
                "evaluation summary not found; run "
                "`uv run python scripts/eval_golden_set.py` first"
            ),
        )
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise HTTPException(status_code=500, detail=f"invalid evaluation summary: {exc}") from exc


def _to_dashboard_summary(raw: dict[str, Any], source_path: Path) -> dict[str, Any]:
    dataset = raw.get("dataset_profile") or {}
    sql = raw.get("sql_contract") or {}
    memory = raw.get("memory_contract") or {}
    rag = raw.get("rag_contract") or {}
    prediction = raw.get("prediction_metrics") or {}
    declared = raw.get("declared_validation_summary") or {}

    generated_at = str(declared.get("generated_at") or "")
    if "T" in generated_at:
        generated_at = generated_at.replace("T", " ")[:16]

    return {
        "title": "Golden Set 评估摘要",
        "generatedAt": generated_at or "-",
        "source": str(source_path),
        "note": "仅展示聚合指标；原始 Golden Set 与评估结果文件不提交远程。",
        "dataset": {
            "dialogues": dataset.get("dialogue_count", 0),
            "turns": dataset.get("turn_count", 0),
            "sqlExpectations": dataset.get("sql_expectation_count", 0),
            "ragEvidence": dataset.get("rag_evidence_count", 0),
            "memoryStates": dataset.get("memory_state_count", 0),
        },
        "contractMetrics": [
            {
                "label": "SQL 模板通过率",
                "value": _percent(sql.get("template_valid_rate")),
                "detail": f"{sql.get('template_valid_count', 0)} / {sql.get('template_count', 0)}",
            },
            {
                "label": "写操作违规",
                "value": str(sql.get("write_operation_violations", 0)),
                "detail": "UPDATE/DELETE/DROP 等",
            },
            {
                "label": "SELECT * 违规",
                "value": str(sql.get("select_star_violations", 0)),
                "detail": "SQL 合约检查",
            },
            {
                "label": "需继承记忆轮次",
                "value": str(memory.get("requires_memory_use_turns", 0)),
                "detail": "多轮条件继承",
            },
            {
                "label": "需忽略记忆轮次",
                "value": str(memory.get("requires_memory_ignore_turns", 0)),
                "detail": "条件覆盖/清空",
            },
            {
                "label": "RAG 证据轮次",
                "value": str(rag.get("evidence_count", 0)),
                "detail": "规范证据召回",
            },
        ],
        "predictionMetrics": [
            {
                "label": "Prediction 数量",
                "value": str(prediction.get("prediction_count", 0)),
                "detail": "prediction JSONL",
            },
            {
                "label": "SQL 校验率",
                "value": _percent(prediction.get("sql_validation_rate")),
                "detail": "sqlglot AST",
            },
            {
                "label": "SQL 表召回",
                "value": _percent(prediction.get("sql_table_recall")),
                "detail": "expected_tables",
            },
            {
                "label": "SQL 过滤值召回",
                "value": _percent(prediction.get("sql_filter_value_recall")),
                "detail": "expected_filters",
            },
            {
                "label": "RAG Recall@5",
                "value": _percent(prediction.get("rag_recall_at_5")),
                "detail": "retrieved top-5",
            },
        ],
        "scenarioCounts": [
            {"label": key, "value": value}
            for key, value in (dataset.get("scenario_counts") or {}).items()
        ],
    }


@router.get("/latest", summary="最新 Golden Set 评估摘要")
async def latest_evaluation() -> dict[str, Any]:
    path = get_settings().eval_results_path
    raw = _load_eval_file(path)
    return _to_dashboard_summary(raw, path)
